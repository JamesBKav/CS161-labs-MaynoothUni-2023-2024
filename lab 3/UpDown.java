public class UpDown
{
    public static void main (String [] args)
    {
        int j = 10;
        while (j <= 20)
        {
            System.out.print(j+" ");
            j++;
        }

        System.out.println();

        for (int i=20; i <= 10; i--)
        {
            System.out.print(i+" ");
        }
    }
}
